package main
import "fmt"

func main() {
  fmt.Println("1 > 2", 1 > 2)
  fmt.Println("1 < 2", 1 < 2)
  fmt.Println("1 >= 2", 1 >= 2)
  fmt.Println("1 <= 2", 1 <= 2)
  fmt.Println("1 == 2", 1 == 2)
  fmt.Println("1 != 2", 1 != 2)
  fmt.Println("1 == 1", 1 == 1)
  fmt.Println("1 != 1", 1 != 1)
}
